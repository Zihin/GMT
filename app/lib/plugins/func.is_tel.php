/**
 * 检测是tel格式是否正确
 * @param string $tel 电话号码
 * @return bool
 */
function func_is_tel($tel){
    if(preg_match('/^1\d{10}$|^(0\d{2,3}-?|\(0\d{2,3}\))?[1-9]\d{4,7}(-\d{1,8})?$/', $tel))
        return true;
    return false;
}
